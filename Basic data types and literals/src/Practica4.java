void main() {
    // один оператор +, разные типы
    String result = "Число: " + 3 +
                    ", начение:" + true +
                    ", float: " + 35.5 +
                    ", bool" + true +
                    ", null" + null;

    String sum = "sum" + (5+3);

    System.out.println(result);

    System.out.println(sum);
}