void main() {
    // Цикл от 1 до 10
    for (int i = 1; i <= 10; i++) {

        // Пропускаем все чётные числа
        if (i % 2 == 0) {
            continue; // переходим к следующей итерации
        }

        // Если число больше 7, прекращаем цикл
        if (i > 7) {
            break; // полностью выходим из цикла
        }

        System.out.println("Нечётное число: " + i);
    }

    System.out.println("Цикл завершён");
}